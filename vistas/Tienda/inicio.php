    <h3 class="text-center text-light">El desafio del agua en Mendoza</h3>
    <p class="text-justify">Mendoza, una provincia conocida por sus paisajes áridos y su economía agrícola, enfrenta un desafío crítico: <strong class="text-warning">la escasez de agua</strong>. En una región donde el agua es un recurso esencial para la vida y el desarrollo, el cambio climático, la mala gestión y el crecimiento poblacional han puesto una presión sin precedentes sobre sus fuentes hídricas. Hoy más que nunca, es fundamental comprender esta problemática y trabajar juntos para encontrar soluciones que aseguren el acceso equitativo y sostenible al agua, tanto para las generaciones presentes como futuras.</h6>
