     <article>
        <h3 class="text-center text-light">Colaboración Ciudadana</h3>
        <p> Cada dia mas necesitamos el agua, quiza no podamos poner en practica acciones mas grandes para la preservacion de nuestro agua, pero cada persona como ciudadanos podemos hacer una gran diferencia empezando por nuestras casas:</p>
        <ul>
            <li><strong class="text-success">Reduce el consumo en casa:</strong> Pequeños cambios en nuestras rutinas pueden tener un gran impacto. Cerrá el grifo mientras te cepillás los dientes, tomá duchas más cortas y usá la lavadora o el lavavajillas solo con cargas completas.</li>
            <li><strong class="text-success">Apuesta por tecnologías eficientes:</strong> Instalar dispositivos como aireadores en los grifos, inodoros de doble descarga y sistemas de riego por goteo puede ayudarte a usar solo la cantidad de agua que realmente necesitás.</li>
            <li><strong class="text-success">Evita el desperdicio:</strong>  Reparar fugas en tuberías y grifos es esencial. Un pequeño goteo puede desperdiciar litros de agua al día sin que lo notemos. Además, reutilizá el agua siempre que sea posible, por ejemplo, para regar plantas o limpiar exteriores.</li>
            <li><strong class="text-success">Opta por productos que usen menos agua:</strong> Al elegir productos agrícolas y manufacturados, informate sobre aquellos que respeten el uso sostenible del agua en su producción. Apoyar prácticas responsables en el consumo es una forma poderosa de influir en el cambio.</li>
            <li><strong class="text-success">Educá y concienciá:</strong> Compartir información con tu comunidad y tu familia sobre la importancia del ahorro de agua es clave para generar un impacto mayor. Mientras más personas estén conscientes del problema, más grande será el esfuerzo colectivo para cuidar este recurso.</li>
            <li><strong class="text-success">Participá en iniciativas comunitarias:</strong> Desde campañas de concientización hasta la promoción de políticas públicas que garanticen una mejor gestión del agua, involucrarse a nivel local y exigir una administración responsable de este recurso es crucial para lograr cambios a largo plazo.</li>
        </ul>
