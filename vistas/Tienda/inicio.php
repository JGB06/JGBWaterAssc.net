        <h3 class="text-center text-light">¿Hay alguna solución?</h3>
        <p> Si bien es cierto no existe una solucion concreta y por mas que existiese, nada nos asegura que se lleve a cabo. Sin embargo podemos proponer posibles soluciones para esta problematica, algunas posibles soluciones pueden ser: </p>

        <ul>
            <li><strong class="text-warning">Tecnología de riego eficiente:</strong> Implementar métodos como el riego por goteo puede reducir significativamente el consumo de agua en la agricultura.</li>
            <li><strong class="text-warning">Reciclaje de agua:</strong> Fomentar el tratamiento y reutilización de aguas residuales tanto en industrias como en comunidades urbanas.</li>
            <li><strong class="text-warning">Educación y concienciación:</strong> Campañas dirigidas a ciudadanos y agricultores sobre el uso responsable del agua, promoviendo prácticas diarias que reduzcan el desperdicio.</li>
            <li><strong class="text-warning">Inversiones en infraestructura verde:</strong> La construcción de sistemas de captación de agua de lluvia y la restauración de acuíferos subterráneos son claves para asegurar un suministro de agua más sostenible.
            </li>
            <li><strong class="text-warning">Gestion Gubernamental:</strong> Revision y modernización de las politicas de gestión del agua que aseguren una mayor participacion ciudadada con un monitoreo constante</li>
        </ul>

